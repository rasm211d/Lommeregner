public class Lommeregner {
    public int sum(int a, int b) {
        return a + b;
    }
    public int minus(int a, int b) {
        return a - b;
    }
    public int gange(int a, int b) {
        return a * b;
    }
    public int dividere(int a, int b) {
        return a / b;
    }
    public int secret(int a, int b) {
        return 2 * a / 4 * b;
    }


}
